        "        +   '<div style=\"font-size:21px;line-height:1.6;font-weight:600;color:var(--text-primary,#1c1e21);margin-bottom:14px;\">' + esc(note.zh) + '</div>'\n"
        "        +   '<div style=\"font-size:14px;line-height:1.55;font-style:italic;color:var(--text-secondary,#6b7280);max-width:30em;margin:0 auto;\">' + esc(note.en) + '</div>'\n"
        "        +   '<div style=\"margin-top:18px;font-size:11px;color:var(--text-tertiary,#9aa0a6);\">窮理查年鑑 · Poor Richard\\'s Almanack · #' + note.id + '</div>'\n"
        "        + '</div>';\n"
        "      UI.showModal('每天一智 · Daily Wisdom', html, [\n"
        "        { label: '收到 · Got it', type: 'primary', action: 'UI.hideModal()' }\n"
        "      ]);\n"
        "      // Auto-close after 10s unless the user already dismissed it.\n"
        "      setTimeout(function () {\n"
        "        try { (window.UI && UI.hideModal) && UI.hideModal(); } catch (e) {}\n"
