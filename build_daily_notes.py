# -*- coding: utf-8 -*-
"""
build_daily_notes.py — parse Poor Richard's Almanack note transcripts into the
daily-note data chunk consumed by the CRM login popup.

Each aphorism in the source is a numbered Chinese line ("N. 中文") immediately
followed by an indented English line. Numbering restarts per 富心法 section, so we
assign a GLOBAL sequential id (the rotation pointer) that must stay stable across
re-runs — append new note files at the END of SOURCES so existing ids never shift.

Re-run after dropping in new transcripts:
    python build_daily_notes.py
Outputs: chunks/script-daily-note.js  (un-minified source; build.mjs makes .min)
"""
import json
import os
import re

ROOT = os.path.dirname(os.path.abspath(__file__))

# Order matters: ids are assigned in this order. Append new files, never reorder.
SOURCES = [
    r"C:\Users\DC\Desktop\honor_notes_20260628\窮理查年鑑_筆記_20260628.txt",
]

NUM_RE = re.compile(r"^\s*(\d+)\.\s+(.*\S)\s*$")


def is_structural(line):
    s = line.strip()
    return (not s) or s.startswith("#") or s.startswith("●") or s.startswith("=") \
        or bool(NUM_RE.match(line))


def parse_file(path, notes):
    chapter = ""
    section = ""
    with open(path, encoding="utf-8") as f:
        lines = f.readlines()

    i = 0
    n = len(lines)
    while i < n:
        line = lines[i].rstrip("\n")
        s = line.strip()
        if s.startswith("## "):
            chapter = s[3:].strip()
            i += 1
            continue
        if s.startswith("### "):
            section = s[4:].strip().strip("【】").replace("】", " ").replace("【", "")
            i += 1
            continue
        m = NUM_RE.match(line)
        if m:
            zh = m.group(2).strip()
            en = ""
            # look ahead for an English continuation line (non-structural)
            j = i + 1
            while j < n and not lines[j].strip():
                j += 1
            if j < n and not is_structural(lines[j]):
                en = lines[j].strip()
            # Bilingual only: skip Chinese-only entries (e.g. 富蘭克林守則 virtues)
            if en:
                notes.append({
                    "id": len(notes) + 1,
                    "zh": zh,
                    "en": en,
                    "chapter": chapter,
                    "section": section,
                })
        i += 1


def main():
    notes = []
    for src in SOURCES:
        if not os.path.exists(src):
            print("WARN missing source:", src)
            continue
        parse_file(src, notes)

