        "      var html = ''\n"
        "        + '<div class=\"daily-note-card\" style=\"text-align:center;padding:8px 4px 4px;\">'\n"
        "        +   '<div style=\"font-size:18px;font-weight:700;letter-spacing:.04em;color:var(--text-primary,#1c1e21);margin-bottom:18px;\">#' + hx.n + ' ' + esc(hx.name) + ' <span style=\"font-weight:500;font-style:italic;color:var(--text-secondary,#8a8f98);\">' + esc(hx.py) + '</span></div>'\n"
        "        +   '<div style=\"height:1px;background:var(--border-color,#e9ebee);margin:0 auto 18px;max-width:78%;\"></div>'\n"
